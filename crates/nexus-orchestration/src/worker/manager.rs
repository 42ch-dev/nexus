/// Describes how to launch a worker process.
///
/// Extended in WS-E T4 to support multi-agent configuration:
/// - `creator_id` — used for `WorkerRegistry` indexing.
/// - `agents` — list of `WorkerAgentConfig` for multi-agent workers.
///
/// Backward compatibility: existing code that creates `WorkerSpec` without
/// these fields still compiles (using `Default::default()` or builder pattern).
#[derive(Debug, Clone, Default)]
pub struct WorkerSpec {
    /// Path to the worker binary or script.
    pub program: String,
    /// Arguments passed to the worker program.
    pub args: Vec<String>,
    /// Environment variables set for the worker process.
    pub env: Vec<(String, String)>,
    /// Current working directory for the worker.
    pub working_dir: Option<String>,
    /// Creator ID for `WorkerRegistry` indexing (WS-E T4).
    pub creator_id: String,
    /// Multi-agent configuration for this worker (WS-E T4).
    pub agents: Vec<WorkerAgentConfig>,
}

impl WorkerSpec {
    /// Create a spec from a program path (no extra args).
    ///
    /// Backward-compatible: sets `creator_id` and `agents` to defaults.
    pub fn from_program(program: impl Into<String>) -> Self {
        Self {
            program: program.into(),
            args: Vec::new(),
            env: Vec::new(),
            working_dir: None,
            creator_id: String::new(),
            agents: Vec::new(),
        }
    }

    /// Test helper — creates a spec for a shell script at the given path.
    pub fn test_stub(path: &str) -> Self {
        Self::from_program("bash").with_arg(path)
    }

    /// Add an argument.
    pub fn with_arg(mut self, arg: impl Into<String>) -> Self {
        self.args.push(arg.into());
        self
    }

    /// Add an environment variable.
    pub fn with_env(mut self, key: impl Into<String>, value: impl Into<String>) -> Self {
        self.env.push((key.into(), value.into()));
        self
    }

    /// Add a working directory.
    pub fn with_working_dir(mut self, dir: impl Into<String>) -> Self {
        self.working_dir = Some(dir.into());
        self
    }

    /// Set the creator ID for registry indexing (WS-E T4).
    pub fn with_creator_id(mut self, creator_id: impl Into<String>) -> Self {
        self.creator_id = creator_id.into();
        self
    }

    /// Set the agents list for multi-agent workers (WS-E T4).
    pub fn with_agents(mut self, agents: Vec<WorkerAgentConfig>) -> Self {
        self.agents = agents;
        self
    }

    /// Add a single agent config (builder-style, WS-E T4).
    pub fn with_agent(mut self, agent: WorkerAgentConfig) -> Self {
        self.agents.push(agent);
        self
    }

    /// Build `--agent-ref` CLI arguments from the agents list.
    ///
    /// For each agent in `self.agents`, generates a `--agent-ref` argument
    /// in the format `role:acp_agent_id[:model]` (WS-E §7.6).
    ///
    /// Returns an empty vector if `self.agents` is empty.
    pub fn build_agent_ref_args(&self) -> Vec<String> {
        self.agents.iter().map(|a| a.to_agent_ref_arg()).collect()
    }
}

