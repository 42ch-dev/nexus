/// Handle to a running worker process.
///
/// Owns an [`IpcClient`] for multiplexed IPC and a cancellation token.
/// The `Child` itself is owned by a background supervisor task.
///
/// ## Backward compatibility
///
/// The legacy `call_json_rpc` method is preserved — it delegates to the
/// internal `IpcClient::call`. New code should prefer using
/// [`WorkerHandle::ipc_client`] directly for concurrent requests.
pub struct WorkerHandle {
    /// PID of the worker process.
    pid: u32,
    /// Cancellation token — fires on shutdown or crash.
    cancel: CancellationToken,
    /// Broadcast sender for events (used by IpcClient for notification routing in WS3).
    #[allow(dead_code)]
    event_tx: broadcast::Sender<WorkerEvent>,
    /// Grace period for shutdown (default: 30 seconds, configurable via spec).
    shutdown_grace: Duration,
    /// Persistent IpcClient for multiplexed JSON-RPC communication.
    ipc: IpcClient,
    /// Whether a clean shutdown has been requested.
    shutdown_requested: bool,
}

impl WorkerHandle {
    /// Create a `WorkerHandle` from an [`IpcClient`] for testing.
    ///
    /// Uses PID 0 (sentinel for mock workers), a fresh cancellation token,
    /// and a no-op broadcast channel. The shutdown grace period is set to
    /// 1 second to keep tests fast.
    /// Create a `WorkerHandle` from an [`IpcClient`] for testing.
    ///
    /// Uses PID 0 (sentinel for mock workers), a fresh cancellation token,
    /// and a no-op broadcast channel. The shutdown grace period is set to
    /// 1 second to keep tests fast.
    ///
    /// This is only intended for integration tests; production code should
    /// obtain handles via [`WorkerManager::spawn`].
    pub fn from_ipc_for_test(ipc: IpcClient) -> Self {
        let (event_tx, _) = broadcast::channel(1);
        Self {
            pid: 0,
            cancel: CancellationToken::new(),
            event_tx,
            shutdown_grace: Duration::from_secs(1),
            ipc,
            shutdown_requested: false,
        }
    }

    /// Send a JSON-RPC request to the worker and await the response.
    ///
    /// Delegates to the internal [`IpcClient`]. This method can be called
    /// multiple times — the underlying `IpcClient` supports concurrent
    /// in-flight requests.
    ///
    /// For concurrent requests, prefer [`WorkerHandle::ipc_client`] which
    /// returns `&IpcClient` for direct use with multiple outstanding calls.
    pub async fn call_json_rpc(&self, method: &str, params: Value) -> Result<Value, WorkerError> {
        self.ipc
            .call(method, params)
            .await
            .map_err(|e| WorkerError::Ipc(e.to_string()))
    }

    /// Request a graceful shutdown of the worker.
    ///
    /// Sends a `worker/shutdown` JSON-RPC request and fires the cancellation
    /// token. The supervisor task will send SIGTERM first, wait for the grace
    /// period, then SIGKILL if needed (WS2 R4).
    pub async fn shutdown(&mut self) -> Result<(), WorkerError> {
        self.shutdown_requested = true;
        self.cancel.cancel();

        info!(
            pid = self.pid,
            grace_ms = self.shutdown_grace.as_millis(),
            "requesting worker shutdown via cancellation token"
        );

        // Try to send a shutdown RPC via IpcClient.
        if !self.ipc.is_closed() {
            let grace_ms = self.shutdown_grace.as_millis() as u32;
            match self
                .ipc
                .call("worker/shutdown", serde_json::json!({"grace_ms": grace_ms}))
                .await
            {
                Ok(_) => {
                    info!(pid = self.pid, "worker acknowledged shutdown RPC");
                }
                Err(e) => {
                    // Worker may have already exited — this is fine.
                    warn!(pid = self.pid, error = %e, "shutdown RPC failed (worker may have already exited)");
                }
            }
        } else {
            info!(
                pid = self.pid,
                "IPC client already closed — supervisor will send SIGTERM"
            );
        }

        // Close the IpcClient to clean up the background reader.
        self.ipc.close().await;

        Ok(())
    }

    /// Return a reference to the internal [`IpcClient`].
    ///
    /// Use this for concurrent multiplexed requests — multiple callers can
    /// call [`IpcClient::call`] on the returned reference simultaneously.
    pub fn ipc_client(&self) -> &IpcClient {
        &self.ipc
    }

    /// Return the process ID.
    pub fn pid(&self) -> u32 {
        self.pid
    }

    /// Return a reference to the cancellation token.
    pub fn cancel_token(&self) -> CancellationToken {
        self.cancel.clone()
    }

    /// Return the configured shutdown grace period.
    pub fn shutdown_grace(&self) -> Duration {
        self.shutdown_grace
    }

    // ---------------------------------------------------------------------------
    // WS-E T4: Multi-agent IPC methods
    // ---------------------------------------------------------------------------

    /// Start a new agent session in this worker.
    ///
    /// Sends `worker/agent_start` IPC method to the worker process.
    /// Returns a summary of the newly created agent session.
    ///
    /// # Arguments
    ///
    /// * `config` — agent configuration to pass to the worker.
    ///
    /// # Errors
    ///
    /// Returns `WorkerError::Ipc` if the session already exists or the worker
    /// fails to start the agent.
    ///
    /// # T8: System Prompt
    ///
    /// If `config.system_prompt` is set, it is sent via IPC and injected
    /// as the first message when the worker creates the ACP session.
    pub async fn agent_start(
        &self,
        config: &WorkerAgentConfig,
    ) -> Result<AgentSessionSummary, WorkerError> {
        let params = serde_json::json!({
            "session_id": config.session_id,
            "acp_agent_id": config.acp_agent_id,
            "role": config.role,
            "model": config.model,
            // T8: system_prompt (daemon reads from preset, worker injects)
            "system_prompt": config.system_prompt,
        });

        let response = self.call_json_rpc("worker/agent_start", params).await?;

        // Parse the response: { "session": { ... } }
        let session_obj = response.get("session").cloned().ok_or_else(|| {
            WorkerError::Ipc("agent_start response missing 'session' field".to_string())
        })?;

        let summary: AgentSessionSummary = serde_json::from_value(session_obj)
            .map_err(|e| WorkerError::Ipc(format!("failed to parse agent_start response: {e}")))?;

        Ok(summary)
    }

    /// Stop an agent session in this worker.
    ///
    /// Sends `worker/agent_stop` IPC method to the worker process.
    /// The agent subprocess is terminated and the session slot is removed.
    ///
    /// # Arguments
    ///
    /// * `session_id` — which session to stop.
    /// * `grace_ms` — grace period for agent subprocess shutdown.
    ///
    /// # Errors
    ///
    /// Returns `WorkerError::Ipc` if the session does not exist.
    pub async fn agent_stop(&self, session_id: &str, grace_ms: u32) -> Result<(), WorkerError> {
        let params = serde_json::json!({
            "session_id": session_id,
            "grace_ms": grace_ms,
        });

        self.call_json_rpc("worker/agent_stop", params).await?;

        Ok(())
    }

    /// List all active agent sessions in this worker.
    ///
    /// Sends `worker/agent_list` IPC method to the worker process.
    /// Returns summaries of all currently active agent sessions.
    ///
    /// # Errors
    ///
    /// Returns `WorkerError::Ipc` on IPC failure.
    pub async fn agent_list(&self) -> Result<Vec<AgentSessionSummary>, WorkerError> {
        let response = self
            .call_json_rpc("worker/agent_list", serde_json::json!({}))
            .await?;

        // Parse the response: { "sessions": [...] }
        let sessions_value = response.get("sessions").cloned().ok_or_else(|| {
            WorkerError::Ipc("agent_list response missing 'sessions' field".to_string())
        })?;

        let summaries: Vec<AgentSessionSummary> = serde_json::from_value(sessions_value)
            .map_err(|e| WorkerError::Ipc(format!("failed to parse agent_list response: {e}")))?;

        Ok(summaries)
    }
}

