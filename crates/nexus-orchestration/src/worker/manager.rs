/// Summary of an agent session, returned by `worker/agent_list` and `worker/agent_start`.
///
/// Matches the worker-side `session_summary_json` output from WS-E T3.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct AgentSessionSummary {
    /// Session identifier within the worker.
    pub session_id: String,
    /// ACP agent ID (from ACP Registry).
    pub acp_agent_id: String,
    /// Role/persona assignment (optional).
    pub role: Option<String>,
    /// Model override (optional).
    pub model: Option<String>,
    /// Current state: "initializing", "ready", "prompting", "error", "stopping", "stopped".
    pub state: String,
    /// Milliseconds since session creation.
    pub uptime_ms: u64,
}

impl AgentSessionSummary {
    /// Check if this session is in a ready state.
    pub fn is_ready(&self) -> bool {
        self.state == "ready"
    }

    /// Check if this session is in an error state.
    pub fn is_error(&self) -> bool {
        self.state == "error"
    }
}

impl Drop for WorkerHandle {
    fn drop(&mut self) {
        if !self.shutdown_requested {
            debug!(
                pid = self.pid,
                "WorkerHandle dropped without explicit shutdown — cancelling"
            );
            self.cancel.cancel();
        }
    }
}

