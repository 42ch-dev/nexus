/// Configuration for one agent slot within a multi-agent worker.
///
/// Used by the daemon to construct the `worker/initialize` payload with
/// `acp_agent_id` / `role` / `model` separation (WS-E §7.2).
///
/// This is the **runtime type** assembled by the daemon from preset roles +
/// user config + CLI flags, not a user-facing config file type.
///
/// ## T8: System Prompt Injection
///
/// `system_prompt` contains the role's system prompt content (read by daemon
/// from `system_prompt_file` in the preset). It is sent via IPC to the worker,
/// which injects it as the first message when creating the ACP session.
#[derive(Debug, Clone, Default, serde::Serialize, serde::Deserialize)]
pub struct WorkerAgentConfig {
    /// Unique session ID for this agent slot (runtime-generated by daemon).
    pub session_id: String,

    /// Which ACP agent to launch — references an agent ID from the ACP Registry.
    /// Example: "claude-acp", "codex-acp", "gemini".
    pub acp_agent_id: String,

    /// Which role/persona to use when starting the ACP session.
    /// References a role defined in the preset's `roles` section.
    pub role: Option<String>,

    /// Which LLM model to use.
    /// Example: "claude-sonnet-4-20250514", "o3", "gemini-2.5-pro".
    pub model: Option<String>,

    /// Optional ACP session ID to resume (session/load).
    pub acp_session_id: Option<String>,

    /// Tool policy override for this agent slot.
    pub tool_policy: Option<String>,

    /// System prompt content for the role (T8).
    /// Daemon reads from role's `system_prompt_file` in the preset and sends
    /// via IPC. Worker injects as first message when creating ACP session.
    /// Missing/invalid file = None (graceful handling, no error).
    pub system_prompt: Option<String>,
}

impl WorkerAgentConfig {
    /// Create a minimal config with required fields.
    pub fn new(session_id: String, acp_agent_id: String) -> Self {
        Self {
            session_id,
            acp_agent_id,
            role: None,
            model: None,
            acp_session_id: None,
            tool_policy: None,
            system_prompt: None,
        }
    }

    /// Builder-style role assignment.
    pub fn with_role(mut self, role: String) -> Self {
        self.role = Some(role);
        self
    }

    /// Builder-style model override.
    pub fn with_model(mut self, model: String) -> Self {
        self.model = Some(model);
        self
    }

    /// Builder-style ACP session ID for resume.
    pub fn with_acp_session_id(mut self, acp_session_id: String) -> Self {
        self.acp_session_id = Some(acp_session_id);
        self
    }

    /// Builder-style tool policy override.
    pub fn with_tool_policy(mut self, tool_policy: String) -> Self {
        self.tool_policy = Some(tool_policy);
        self
    }

    /// Builder-style system prompt (T8).
    /// Daemon reads from preset's `system_prompt_file` and passes here.
    pub fn with_system_prompt(mut self, system_prompt: String) -> Self {
        self.system_prompt = Some(system_prompt);
        self
    }

    /// Format this config as a `--agent-ref` CLI argument.
    ///
    /// Format: `role:acp_agent_id[:model]` (matches WS-E §7.6).
    /// If `role` is absent, uses the session_id as the key prefix.
    pub fn to_agent_ref_arg(&self) -> String {
        let role_key = self.role.as_ref().unwrap_or(&self.session_id);
        let mut parts = vec![role_key.clone(), self.acp_agent_id.clone()];
        if let Some(model) = &self.model {
            parts.push(model.clone());
        }
        parts.join(":")
    }
}

